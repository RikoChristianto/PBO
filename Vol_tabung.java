package week1;

public class Vol_tabung {

	public static void main(String[] args) {
		double diameter = 5;
        double tinggi = 10;

        double volume = Math.PI * Math.pow(diameter / 2, 2) * tinggi;

        System.out.println("Volume tabung dengan diameter 5 dan tinggi 10 adalah " + volume);

	}

}
